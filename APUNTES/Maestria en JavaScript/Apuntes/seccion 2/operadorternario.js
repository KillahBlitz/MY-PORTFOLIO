//operador internario
// El operador ternario es una forma abreviada de escribir una sentencia if-else.
// Se utiliza el signo de interrogación (?) para separar la condición del resultado verdadero y falso.
let resultado = (1>2) ? "Verdader" : "Falso";
console.log(resultado); // El numero 1 no es mayor que el numero 2