//operaciones de asignacion
let a = 1;

// Asignacion simple
a += 3; // a = a + 3
console.log(a); // 4

// Asignacion de resta
a -= 2; // a = a - 2
console.log(a); // 2

// Asignacion de multiplicacion
a *= 2; // a = a * 2
console.log(a); // 4

// Asignacion de division
a /= 2; // a = a / 2
console.log(a); // 2

// Asignacion de modulo
a %= 2; // a = a % 2
console.log(a); // 0

// Asignacion de exponenciacion
a **= 3; // a = a ** 3
console.log(a); // 0