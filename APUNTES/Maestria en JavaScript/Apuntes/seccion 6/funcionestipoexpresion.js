//declaracion de funciones tipo expresion
//las funciones tipo expresion son funciones que se asignan a una variable
//declaracion de la funcion
let suma = function(a, b){return a+b} //declaracion de la funcion, en () se pueden poner los argumentos o parametros

//llamada a la funcion
let z = suma(5, 10); //llamada a la funcion, se le pasan los argumentos 5 y 10 y se guarda el resultado en la variable b
console.log("suma: " + z); //muestra el resultado de la suma en la consola