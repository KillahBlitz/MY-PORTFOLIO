//sentencia if else y su uso

let condicion = false; // variable booleana

if (condicion) { // si la condicion es verdadera
    console.log("La condicion es verdadera"); // se ejecuta este bloque
}
else { // si la condicion es falsa
    console.log("La condicion es falsa"); // se ejecuta este bloque
}

