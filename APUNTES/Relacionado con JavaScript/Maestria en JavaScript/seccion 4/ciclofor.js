//explicacion del ciclo for en javascript
// El ciclo for se utiliza para ejecutar un bloque de código un número específico de veces. La sintaxis es la siguiente:
// for (inicialización; condición; incremento/decremento) {
//     // bloque de código a ejecutar
// }

//este se ocupa para recorrer arreglos o listas
for (let i=0; i<=5; i++) { // El ciclo se ejecuta mientras i sea menor que 5
    console.log("Me imprimo 5 veces"); // Mostramos el valor de i en la consola
    contador++; // Incrementamos el contador en 1
}
// En este caso, el ciclo se ejecutará 5 veces, mostrando los números del 1 al 5 en la consola. Una vez que i sea mayor que 5, el ciclo se detendrá y no se ejecutará más.
// En resumen, el ciclo for es útil cuando sabemos cuántas veces queremos que se ejecute un bloque de código y queremos controlar el flujo de ejecución de manera precisa.